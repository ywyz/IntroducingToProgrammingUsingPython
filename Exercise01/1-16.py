import turtle

turtle.penup()
turtle.goto(-100, -100)
turtle.pendown()
turtle.circle(100)

turtle.penup()
turtle.goto(100, -100)
turtle.pendown()
turtle.circle(100)

turtle.penup()
turtle.goto(100, 100)
turtle.pendown()
turtle.circle(100)

turtle.penup()
turtle.goto(-100, 100)
turtle.pendown()
turtle.circle(100)

turtle.done()
